                y = ymouse;
            } else {
                dx = (xmouse - x) * 0.125;
                dy = (ymouse - y) * 0.125;
                if (Math.abs(dx) + Math.abs(dy) < 0.1) {
                    x = xmouse;
                    y = ymouse;
                } else {
                    x += dx;
                    y += dy;
                }
            }
            ball.style.left = x + 'px';
            ball.style.top = y + 'px';
        };
    followMouse();
};

